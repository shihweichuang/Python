sum += int(x[7]) * 2   # 第8個字轉為整數，乘以2
sum += int(x[8]) * 1   # 第9個字轉為整數，乘以1
sum += int(x[9]) * 1   # 第10個字轉為整數，乘以1

if sum % 10 == 0:      # 如果加總可以被10整除，列印合法
    print('合法')
else:                  # 如果加總無法被10整除，列印不合法
    print('不合法')