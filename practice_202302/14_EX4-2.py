# # 【EX4-2 畫直角三角形】
#
# # 使用者會輸入一個大於或等於1的整數，
# # 按照數字輸出用星號畫出圖案
#
# # 輸入 1
# # 輸出 *
#
# 輸入 2
# 輸出 *
#     **
#
# 輸入 3
# 輸出 *
#     **
#     ***
#
# # ---------------------------

x = int(input())     # 輸入(字串)轉為整數

i = 1                # i從1開始
while i <= x:        # i到x結束
    print('*' * i)   # 列印i個*

    i += 1           # i每次+1