# # 【TA2-0 練習 迭代加法】
#
# # 請使用while迴圈，印出指定數字的跌代加總。
# # 例如題目是5，# # 必須輸出從1開始加到5的答案15
# # 1+2+3+4+5=15
#
# # 輸入：5    輸出：15
# # 輸入：10   輸出：55
#
# # ---------------------------

i = 0              # i從0開始
a = int(input())   # 輸入(字串)轉為整數
num_sum = 0        # 總和從0開始

while i <= a:      # i到a結束
  num_sum +=  i    # 總和每次加上前一次i

  i += 1           # i每次+1

print(num_sum)     # 印出最後加總